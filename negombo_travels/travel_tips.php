		<p><strong>What Is A Sri Lankan Visa?</strong><br />
	    A Sri Lankan visa is an endorsement on a passport or a similar document to facilitate the legal entry of non Sri Lankans into the country and to regulate the period of their stay and the conditions governing such stay.</p>
		<p><strong>What Are The Types Of Sri Lankan Visas?</strong><br />
	    There are four kinds of visas which permit a person to enter and/or stay in Sri Lanka.</p>
<p><strong>Visit Visa</strong><br />
  A Visit Visa is an entry permit signifying the consent of the Sri Lankan Government for the admission of a foreign national to the country. The Visa contains details of the period of time and the condition/s of the stay. There are two sub-categories which come under visit visas &ndash; Tourist Visit Visa<br />
  A Tourist Visa is issued to bona-fide tourists who want to enter Sri Lanka for sightseeing, excursions, relaxation, visit relatives or yoga training for a short period of time.</p>
<p><strong>Photo Permits &amp; Entrance Charges</strong></p>
<p>Sri Lanka is a photographer&rsquo;s delight. However, permits are required before you can take photos at certain sites. Entrance tickets to individual sites are available only from 6 a.m. to 10 a.m. These tickets cover charges for photography, recording and parking.Rates are quoted in US Dollars and rupee parities are subject to fluctuation.</p>
<p><strong>Foreign Currency Regulations</strong><br />
  Visitors to Sri Lanka bringing in more than US$10,000 should declare the amount to the Customs on arrival. All unspent rupees converted from foreign currencies can be re-converted to the original currency on departure as long as encashment receipts can be produced.</p>
<p>&nbsp;</p>
<p><strong>Before You Go</strong><br />
  No inoculations are compulsory unless you are coming from a yellow fever or cholera area. (Cholera is very occasionally reported in Sri Lanka, so is not considered a serious risk.) However, the following vaccinations are recommended, particularly if you plan a long trip or intend visiting remote areas:</p>
<p>Typhoid (monovalent), Polio, Tetanus, Hepatitis A, Hepatitis B, Rabies<br />
  Children should, in addition, be protected against:<br />
  diphtheria, whooping cough, mumps, measles, rubella</p>
<p>Remember to plan well ahead with vaccinations. Allow up to six weeks to receive the full course, for some vaccinations require more than one dose, and some should not be given together.</p>
<p>The risk of malaria exists throughout the whole country apart from the districts of Colombo, Kalutara and Nuwara Eliya. Medication has to start one week prior to travel, continue during the trip, and finish four weeks after your return. Once again, planning is essential, as well as care to ensure the course is followed.</p>
<p><strong>What To Wear</strong><br />
  Cotton clothes are useful at any time of the year but you will need light woollens for the hills and waterproof clothing or an umbrella.Modest dress for women is advisable especially off the beach and when visiting religious sites. Don't forget comfortable shoes, sandals or trainers and cotton socks. If you are planning to trek and climb go prepared with suitable gear. Water sports enthusiasts would do well to take their snorkels and diving equipment along.</p>
<p><strong>Drink</strong><br />
  Sri Lankan &lsquo;Ceylon&rsquo; tea is prepared as in the West and coffee too. There are a huge variety of bottled soft drinks, including well-known international brands. Thambili ( king coconut water )is a safe and refreshing option. Local beer and spirits are widely available. Bottled mineral water is available in 5 star hotels. Please note: Alcohol is not sold on Poya (full-moon day of the month) days.</p>
